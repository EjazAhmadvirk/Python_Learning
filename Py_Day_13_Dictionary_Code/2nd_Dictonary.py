# Using curly braces
dict1 = {"a": 1, "b": 2}

# Using dict() function
dict2 = dict(x=10, y=20)

# Empty dictionary
dict3 = {}

# Nested dictionary
dict4 = {
    "student1": {"name": "Ali", "age": 20},
    "student2": {"name": "Ejaz", "age": 21}
}

print(dict1)
print(dict2)
print(dict3)
print(dict4)




