name = "Ejaz Ahmad Virk"
new_name = len(name)
print(new_name)
print(name.upper()) #convert to upper case
print(name.lower()) #convert to lower case
print(name.title()) #convert to title case
print(name.capitalize()) #convert to capitalize case
print(name.swapcase()) #convert to swap case
print(name.count("a")) #count the number of a in the string
print(name.find("a")) #find the first occurrence of a in the string
print(name.replace("a", "A")) #replace a with A in the string
print(name.split()) #split the string into a list
print(name.join("a")) #join the string with a
print(name.strip()) #remove the leading and trailing whitespace from the string
print(name.lstrip()) #remove the leading whitespace from the string
print(name.rstrip()) #remove the trailing whitespace from the string






